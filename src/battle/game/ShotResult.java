package battle.game;

/**
 * The possible results of a shot :
 * <ul>
 *     <li>MISS</li>
 *     <li>HIT</li>
 *     <li>SUNK</li>
 * </ul>
 */
public enum ShotResult {
    /**
     * Miss shot result.
     */
    MISS,
    /**
     * Hit shot result.
     */
    HIT,
    /**
     * Sunk shot result.
     */
    SUNK
}
